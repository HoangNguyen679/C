#include<stdio.h>
int main()
{
  int i;
  double d;
 printf("Enter a integer  number: ");
 scanf("%d",&i);
 printf(" Your number in decimal is %d \n",i);
 printf(" Your number in hexa is %X \n",i);
 printf("Enter a real number: ");
 scanf("%lf",&d);
 printf(" Your number type is  %f \n",d); 
 printf(" Your number in science type is  %E \n",d);
 return 0;
}
